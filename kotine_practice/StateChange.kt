@Composable
fun ColorBox(modifier: Modifier = Modifier) {
    val color = remember {
            mutableStateOf(Color.Yellow)
        }
    Box(modifier = modifier
        .background(Color.DarkGray)
        .clickable {
            color.value = Color(
                Random.nextFloat(),
                Random.nextFloat(),
                Random.nextFloat(),
                1f
            )

        } )

}

